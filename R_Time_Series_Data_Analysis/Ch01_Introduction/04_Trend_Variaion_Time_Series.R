### 시계열 데이터의 자료 형태.

## 추세변동 시계열(Trend Variation Time Series).

# 시간에 따라 증가하거나 또는 감소하는 경향을 보여주는 시계열 형태.
# 이러한 추세변동(Trend Variation, Trend Fluctuation, 경향변동.)

# 수식의 α, β 는  미지의 모수로서 시계열 자료에서 추정.

# 수식의 "α + β1t"는 선형의 추세변동(Linear Trend Variation)을 나타내는 수식.
# 두 번째 수식의 "α + β1t + β2t" 는 2차식의 비선형에 따른 추세변동(Non-Linear Trend variation)을 나타내는 수식.




