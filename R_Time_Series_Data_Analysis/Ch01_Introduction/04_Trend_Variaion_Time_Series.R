dd3 = matrix(c(1142, 1242, 1252, 1343,   1225, 1562, 1356, 1572,
               1343, 1459, 1412, 1453,   1401, 1478, 1322, 1606,
               1554, 1589, 1597, 1408))

dd3.ts = ts(data=dd3, start=c(2006,1), frequency = 4)
dd3.ts

plot(dd3.ts, main = "Trend Variation Time Series")




