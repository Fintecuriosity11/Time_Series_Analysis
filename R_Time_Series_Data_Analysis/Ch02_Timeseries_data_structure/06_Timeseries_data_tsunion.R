### 시계열 자료의 합집합: ts.union

# frequency가 같은 복수의 시계열 자료에 대하여, 합병을 수행할 경우 적용되는 프로그램은 "ts.union".
# 이는 집합에서 "합집합-U"에 해당되며, 자료가 없을 경우에는 "NA"로 나타냄.

zz1=ts(matrix(1:24,8,3), s=c(2001,1),f=4,n=c("a","b","c"))                     # 시계열 자료 1.

zz1

zz2=ts(matrix(11:22,4,3), s=c(2002,1),f=4,n=c("a","b","c"))                    # 시계열 자료 2.
zz2

union(zz1,zz2)                                                                 # 일반적 합집합.

ts.union(zz1,zz2)                                                              # 시계열 합집합.
# 결측값은 NA

graphics.off()                                       # 그래프 지워주는 함수.

