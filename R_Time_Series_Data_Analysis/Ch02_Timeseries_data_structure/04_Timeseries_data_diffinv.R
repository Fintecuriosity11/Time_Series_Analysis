### 역차분(Inverse difference)의 산정.

# 시계열 자료에서 특징시점 자료에서 그 앞에 있는 자료와의 차이를 나타내는 과정을 차분이라고 함.

# 이러한 차분의 산정값을 토대로 원래의 조사자료로 환원하여 산정하는 과정을 역차분(Inverse difference)라고 함.

# 


dd.ts                                                 # 분석자료 yt


(d1=diff(dd.ts))                                     # lag=1, 1차, 차분 산정.

(d2=diffinv(d1))                                     # lag=1, 1차 역차분 산정.

for(i in 1:length(dd.ts)) d3 = d2 +1142              # 초기값: 1142를 고려,
d3                                                   # 원래, 조사자료로 환원.


graphics.off()                                       # 그래프 지워주는 함수.

