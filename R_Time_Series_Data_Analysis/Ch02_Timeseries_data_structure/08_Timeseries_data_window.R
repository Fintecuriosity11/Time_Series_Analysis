### 시계열 자료의 부분추출 및 수정: window.

# Matrix, 또는 시계열 자료에 대한 부분추출 및 수정은 "window" 프로그램을 통하여 수행됨.



zz1                                                                            # 시계열 자료 1.

zz2                                                                            # 시계열 자료 2.

window(zz1, s=c(2001,3), delta=1)                                              # 시계열 자료 1의 2001년  3분기 자료만 추출.

window(zz2, s=c(2002,4), delta=1)                                              # 시계열 자료 2의 2002년  4분기 자료만 추출.

window(zz1,c(2001,3), c(2002,2))                                               # 시계열 자료 1의 2001년  3분기 자료부터 2002년 2분기 자료.

window(zz1,c(2001,1), c(2001,2)) <- c(11,12)                                   # 2001 1분기 자료와 2분기 자료 수정.

zz1



graphics.off()                                       # 그래프 지워주는 함수.

